<h2>Allow Amazon Pay in Firefox</h2>
<p>In current Firefox versions, the Block page elements setting can be set to Strict by default.<br/>This will block Amazon Pay and you won't be able to proceed when selecting the shipping method in the Amazon Checkout.<br /><br />
The window either loads forever:<br />
<img src="images/firefox-amazon-1.jpg" alt="Amazon Pay not loading" border="0" /><br />
Or the message "To continue shopping, your browser needs access to Amazon Pay" appears:<br />
<img src="images/firefox-amazon-4.jpg" alt="To continue shopping, your browser needs access to Amazon Pay" border="0" /><br /><br />
At the top left of the address bar is a shield symbol:<br />
<img src="images/firefox-amazon-2-en.jpg" alt="shield symbol" border="0" /><br /><br />
Mouseover shows that Firefox has blocked elements.<br /><br />
Click on the shield icon and then Turn off Blocking for This Site:<br />
<img src="images/firefox-amazon-3-en.jpg" alt="Turn off Blocking for This Site" border="0" /><br /><br />
Then the Amazon window will be loaded immediately and you can continue the checkout.</p>