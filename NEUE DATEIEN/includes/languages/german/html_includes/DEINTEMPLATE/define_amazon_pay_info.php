<h2>Amazon Pay in Firefox erlauben</h2>
<p>In aktuellen Firefox Versionen kann die Einstellung Seitenelemente blockieren auf Streng voreingestellt sein. <br />Dadurch wird Amazon Pay blockiert und Sie kommen bei der Auswahl der Versandart im Amazon Checkout nicht weiter.<br /><br />
Das Fenster lädt entweder ewig:<br />
<img src="images/firefox-amazon-1.jpg" alt="Amazon Pay lädt nicht" border="0" /><br />
Oder bringt die Meldung "Um den Einkauf fortzusetzen, benötigt Ihr Browser Zugriff auf Amazon Pay":<br />
<img src="images/firefox-amazon-4.jpg" alt="Um den Einkauf fortzusetzen, benötigt Ihr Browser Zugriff auf Amazon Pay" border="0" /><br /><br />
Links oben in der Adresszeile ist ein Schildsymbol:<br />
<img src="images/firefox-amazon-2.jpg" alt="Schildsymbol" border="0" /><br /><br />
Mouseover zeigt, dass Firefox Elemente blockiert hat.<br /><br />
Aufs Schildsymbol clicken und dort dann auf Blockierung für diese Website deaktivieren:<br />
<img src="images/firefox-amazon-3.jpg" alt="Blockierung für diese Website deaktivieren" border="0" /><br /><br />
Dann wird das Amazon Fenster sofort geladen und Sie können den Checkout fortsetzen.</p>
